package com.zazsona.commemorations.exception;

public class UnableToBuildSchematicException extends Exception
{
    public UnableToBuildSchematicException()
    {
        super();
    }

    public UnableToBuildSchematicException(String message)
    {
        super(message);
    }

    public UnableToBuildSchematicException(Throwable cause)
    {
        super(cause);
    }

    public UnableToBuildSchematicException(String message, Throwable cause)
    {
        super(message, cause);
    }
}
